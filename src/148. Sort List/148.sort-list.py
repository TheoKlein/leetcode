# Sort a linked list in O(n log n) time using constant space complexity.
# 
# Example 1:
# 
# 
# Input: 4->2->1->3
# Output: 1->2->3->4
# 
# 
# Example 2:
# 
# 
# Input: -1->5->3->4->0
# Output: -1->0->3->4->5
# 
#
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def sortList(self, head: ListNode) -> ListNode:
        if not head or not head.next:
            return head
        
        # merge sort
        # 1. find middle node of the list
        mid = head
        fast = head.next
        while fast and fast.next:
            fast = fast.next.next
            mid = mid.next
        mid.next, mid = None, mid.next

        # merge divided list
        return self.mergeLists(self.sortList(head), self.sortList(mid))

    def mergeLists(self, l1, l2):
        root = head = ListNode(0)
        while l1 and l2:
            if l1.val < l2.val:
                head.next = l1
                l1 = l1.next
            else:
                head.next = l2
                l2 = l2.next
            head = head.next
        head.next = l1 or l2
        return root.next
